#include "libft.h"

/*

	DESCRIPTION :
	The ft_bzero function sets the first n bytes of the memory area pointed
	to by s to zero, effectively replacing each byte with the value 0.

	RETURN VALUE :
	This function does not return anything (void type).

*/

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		*(unsigned char *)(s + i) = '\0';
		i++;
	}
}
