#include "libft.h"

/*

	DESCRIPTION :
	The ft_putstr_fd function writes the string s to the given file
	descriptor fd.

	RETURN VALUE :
	This function does not return a value.

*/

void	ft_putstr_fd(char *s, int fd)
{
	size_t	len;

	len = ft_strlen(s);
	write(fd, s, len);
}
